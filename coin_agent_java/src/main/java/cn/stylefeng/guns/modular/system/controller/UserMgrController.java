    /**
     * 分配角色
     */
    @RequestMapping("/setRole")
    @BussinessLog(value = "分配角色", key = "userId,roleIds", dict = UserDict.class)
    @Permission
    @ResponseBody
    public ResponseData setRole(@RequestParam("userId") Long userId, @RequestParam("roleIds") String roleIds) {
        if (ToolUtil.isOneEmpty(userId, roleIds)) {
            throw new ServiceException(BizExceptionEnum.REQUEST_NULL);
        }
        if (StringUtils.isNotBlank(roleIds)){
          String [] roles =   roleIds.split(",");
          if(roles != null && roles.length>1){
              return ResponseData.error("只能勾选一个");
          }
        }
        //不能修改超级管理员
        if (userId.equals(Const.ADMIN_ID)) {
            throw new ServiceException(BizExceptionEnum.CANT_CHANGE_ADMIN);
        }
        Long roId = Long.parseLong(roleIds.split(",")[0]);
        Long memberId = null;
        Long otherRoleId = null;
        if (!ShiroKit.isAdmin()){
            memberId=  F.me().getMemberByUser(ShiroKit.getUser().getId());
            if(ShiroKit.getUser().getRoleList() != null && ShiroKit.getUser().getRoleList().size()>0)
                otherRoleId = ShiroKit.getUser().getRoleList().get(0);
        }
        if (memberId  != null && otherRoleId != null){
            Role role = new Role();
            role.setPid(otherRoleId);
           List<Role> roleLis =  roleService.list(new QueryWrapper<>(role));
           if (roleLis == null || roleLis.size()<=0){
               return ResponseData.error("分配失败");
           }
           boolean flag = true;
           for (Role role1 :roleLis){
               if (role1.getRoleId().equals(roId)){
                   flag = false;
                   break;
               }
           }
           if(flag){
               return ResponseData.error("只能分配第一个");
           }
        }
        this.userService.assertAuth(userId);
        this.userService.setRoles(userId, roleIds);
        return SUCCESS_TIP;
    }

    /**
     * 上传图片
     */
    @RequestMapping(method = RequestMethod.POST, path = "/upload")
    @ResponseBody
    public String upload(@RequestPart("file") MultipartFile picture) {

        String pictureName = UUID.randomUUID().toString() + "." + ToolUtil.getFileSuffix(picture.getOriginalFilename());
        try {
            String fileSavePath = gunsProperties.getFileUploadPath();
            picture.transferTo(new File(fileSavePath + pictureName));
        } catch (Exception e) {
            throw new ServiceException(BizExceptionEnum.UPLOAD_ERROR);
        }
        return pictureName;
    }
