    /**
     * 重置密码
     */
    @RequestMapping("/reset")
    @BussinessLog(value = "重置密码", key = "userId", dict = UserDict.class)
    @Permission(Const.ADMIN_NAME)
    @ResponseBody
    public ResponseData reset(@RequestParam Long userId) {
        if (ToolUtil.isEmpty(userId)) {
            throw new ServiceException(BizExceptionEnum.REQUEST_NULL);
        }
        this.userService.assertAuth(userId);
        User user = this.userService.getById(userId);
        Member member = memberService.getById(user.getMemberId());
        if (member!=null) {
            member.setSalt(ShiroKit.getRandomSalt(5));
            member.setPassword(ShiroKit.md5(Const.DEFAULT_PWD, member.getSalt()));
            memberService.updateById(member);
            //redisUtil.del(Constant.SMS + member.getAccount());
        }
        user.setSalt(ShiroKit.getRandomSalt(5));
        user.setPassword(ShiroKit.md5(Const.DEFAULT_PWD, user.getSalt()));
        this.userService.updateById(user);
        return SUCCESS_TIP;
    }
    /**
     * 重置用户资金密码 无用 在memberController
     */
    @RequestMapping("/updatePay")
    @BussinessLog(value = "重置用户资金密码", key = "memberId", dict = UserDict.class)
    @Permission(Const.ADMIN_NAME)
    @ResponseBody
    private ResponseData updatePay(@RequestParam Long memberId){
        if (ToolUtil.isEmpty(memberId)) {
            throw new ServiceException(BizExceptionEnum.REQUEST_NULL);
        }
        Member member = this.memberService.getById(memberId);
        String newSalt = ShiroKit.getRandomSalt(5);
        String newPwdmd5 = ShiroKit.md5(Const.DEFAULT_PAY_PWD, newSalt);
        member.setPayPassword(newPwdmd5).setPaySalt(newSalt).setUpdateUser(member.getMemberId());
        member.setIsOpenPay("Y");
        memberService.updateById(member);
        //redisUtil.del(Constant.SMS + member.getAccount());
        return SUCCESS_TIP;
    }



